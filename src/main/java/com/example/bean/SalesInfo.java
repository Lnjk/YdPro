package com.example.bean;

public class SalesInfo {
	private String name;
	private String xse;
	private String the;
	private String zre;
	private String jxs;
	private String xscb;
	private String xsml;
	private String mll;
	private String cbmll;

	public SalesInfo() {
		super();
	}

	public SalesInfo(String name, String xse, String the, String zre,
			String jxs, String xscb, String xsml, String mll, String cbmll) {
		super();
		this.name = name;
		this.xse = xse;
		this.the = the;
		this.zre = zre;
		this.jxs = jxs;
		this.xscb = xscb;
		this.xsml = xsml;
		this.mll = mll;
		this.cbmll = cbmll;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getXse() {
		return xse;
	}

	public void setXse(String xse) {
		this.xse = xse;
	}

	public String getThe() {
		return the;
	}

	public void setThe(String the) {
		this.the = the;
	}

	public String getZre() {
		return zre;
	}

	public void setZre(String zre) {
		this.zre = zre;
	}

	public String getJxs() {
		return jxs;
	}

	public void setJxs(String jxs) {
		this.jxs = jxs;
	}

	public String getXscb() {
		return xscb;
	}

	public void setXscb(String xscb) {
		this.xscb = xscb;
	}

	public String getXsml() {
		return xsml;
	}

	public void setXsml(String xsml) {
		this.xsml = xsml;
	}

	public String getMll() {
		return mll;
	}

	public void setMll(String mll) {
		this.mll = mll;
	}

	public String getCbmll() {
		return cbmll;
	}

	public void setCbmll(String cbmll) {
		this.cbmll = cbmll;
	}

}
